public class Tipos
{
    public static void main(String[] args)
    {
        // Tipos primitivos são espaços de memorias mais simples
        // usando eles, a aplicação fica um pouco mais leve
        // do que apenas utilizar variáveis normal para receber as informações
        int x = 2;
        Integer y = 4;

        System.out.println(x);
        System.out.println(y);
    }
}
